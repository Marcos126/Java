package ClasesYObjetos;

public class Persona {
    private int edad;
    private String nombre;

    public void setterEdad(int edad){
        this.edad = edad;
    }

    public String getterNombre(){
        return this.nombre;
    }
    public void setterNombre(String nombre){
        this.nombre = nombre;

    }

    public int getterEdad(){
        return this.edad;
    }


}

